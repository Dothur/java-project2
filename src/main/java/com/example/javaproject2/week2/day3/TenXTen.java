import java.util.Arrays;

public class TenXTen {
    public static void main(String[] args) {
        int[][] intArr = new int[10][10]; // 배열 선언과 초기화
        System.out.println(Arrays.toString(intArr[0]));
        System.out.println(Arrays.toString(intArr[1]));
        System.out.println(Arrays.toString(intArr[2]));
        System.out.println(Arrays.toString(intArr[3]));
        System.out.println(Arrays.toString(intArr[4]));
        System.out.println(Arrays.toString(intArr[5]));
        System.out.println(Arrays.toString(intArr[6]));
        System.out.println(Arrays.toString(intArr[7]));
        System.out.println(Arrays.toString(intArr[8]));
        System.out.println(Arrays.toString(intArr[9]));
    }
}
