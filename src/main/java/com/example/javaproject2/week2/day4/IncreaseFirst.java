package com.example.javaproject2.week2.day4;

public class IncreaseFirst {
    public static void main(String[] args) {
        int idx = 0;
        int[] arr = {2, 1, 7, 9};
        System.out.println(arr[idx++]); // arr[0] = 2
        System.out.println(arr[idx++]); // arr[1] = 1
        System.out.println(arr[idx++]); // arr[2] = 7
    }
}
